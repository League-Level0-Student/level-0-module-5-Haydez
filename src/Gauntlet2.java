
public class Gauntlet2 {
public static void main(String[] args) {
	for(int i=0; i<4; i++) {
		for(int a=0; a<4; a++) {
		System.out.println(i+"  "+a);
		}
	}
	
	for(int p=1; p<8; p+=3 ) {
		for(int u=0; u<3; u++ ) {
			
				System.out.print(p+u+"   ");
			
		}
	System.out.print("\n");
	}
	
		
		
	
}
}
